// Get all template categories
export const getTemplateCategories = () => {
  return Object.keys(commonItemTemplates);
};

// Get items for a specific template category
export const getTemplateItems = (category) => {
  return commonItemTemplates[category] || [];
};

// Search templates
export const searchTemplates = (query) => {
  const results = [];
  const lowerQuery = query.toLowerCase();

  Object.entries(commonItemTemplates).forEach(([category, items]) => {
    items.forEach(item => {
      if (item.name.toLowerCase().includes(lowerQuery)) {
        results.push({ ...item, templateCategory: category });
      }
    });
  });

  return results;
};

export default commonItemTemplates;
